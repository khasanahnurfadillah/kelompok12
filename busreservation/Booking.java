    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String customer = jTextField1.getText();
        String contact = jTextField4.getText();
        String from = (String)jComboBox1.getSelectedItem();
        String to = (String)jComboBox2.getSelectedItem();
        int seat = seatNo;
        String price = jTextField3.getText();
        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        Date d1 = jCalendar1.getDate();
        String date = date_format.format(d1); 
                
        try {
            Connection kon=koneksi.koneksiDb();
            
            Statement st = kon.createStatement();
            String query = "select * from tb_booking where FromStation = '"+from+"' and ToStation = '"+to+"' and seatNo='"+seat+"' and date = '"+date+"'";
            ResultSet rs = st.executeQuery(query);
            
            if(rs.next()){      
                JOptionPane.showMessageDialog(this, "Sorry this has been Booked");      
            }
            else if(from.equals(to)){
                JOptionPane.showMessageDialog(this, "Source and Destination can not be same"); 
            }
            else if(d1.before(dateToday())){
                JOptionPane.showMessageDialog(this, "Invalid Date"); 
            }
            else{
                try{ 
                    ResultSet temp = st.executeQuery("Select count(id) from tb_booking");
                    if(temp.next()){
                        myID= temp.getInt(1) + 1;
                    }
                    String insertQuery = "insert into tb_booking values('"+myID+"','"+customer+"','"+contact+"','"+from+"','"+to+"','"+seat+"','"+price+"','"+date+"')";
            int k = st.executeUpdate(insertQuery);
            if(k==1){
                JOptionPane.showMessageDialog(this, "Ticket Booked");
                bill();
            }
                }
                catch (Exception e) {
                    JOptionPane.showMessageDialog(this,e);
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

